package arrayStudy;

public class MultidimensionalArray2 {

	public static void main(String[] args) {

		//String name [][]= {{},{}};
		String name [][]= {{"aa","bb","cc"},{"dd","ee","ff"}};
		
		for(int i=0;i<=1;i++)
		{
			for(int j=0;j<=2;j++)
			{
				
				System.out.print(name[i][j] + " ");
			}
			
			System.out.println();
			
			
		}
		
		
		
		
		
		
		
		
	}

}
